package com.overloadingLab;

public class PredefinedOverloadMethod {

	public static void main(String[] args) {
//print, println, sleep are some predefined overloaded methods		
		System.out.println(10);
		System.out.println("ABC");
		System.out.println(11f);
		System.out.println("--------");
		System.out.print(10);
		System.out.print("ABC");
		System.out.print(11f);

	}

}
